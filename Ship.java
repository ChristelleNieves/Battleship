public class Ship {
    int x;
    int y;

    // Constructor
    // Sets the X and Y coordinates for a ship
    public Ship(int x, int y) {
        this.x = x;
        this.y = y;
    }

    // Return the X coordinate of the ship
    public int getX() {
        return x;
    }

    // Return the Y coordinate of the ship
    public int getY() {
        return y;
    }
}
